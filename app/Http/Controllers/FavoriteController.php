     */
    public function store(Request $request)
    {
        $favorite = Favorite::firstOrNew(['user_id' => Auth::id(), 'company_id' => $request->company_id]);

        if(isset($favorite->id)) {
            return $favorite->delete();
        }
        
        return $favorite->save();
    }

